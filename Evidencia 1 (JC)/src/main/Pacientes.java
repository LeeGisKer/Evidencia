package main;

public class Pacientes {
    private String nombreC;
    private String id;

    public Pacientes(String nombreC, String id) {
        this.nombreC = nombreC;
        this.id = id;
    }

    public Pacientes() {
    }

    public String getNombreC() {
        return nombreC;
    }

    public void setNombreC(String nombreC) {
        this.nombreC = nombreC;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    
    

    

   
    
    
    




}
